def test_mm_ohlcv_job_top_embeds_pipeline_register_offsets() -> None:
    text = generate_mm_ohlcv_job_top_sv()
    assert "module dau_mm_ohlcv_job" in text
    assert f"12'h{int(RegisterOffset.PIPELINE_FILTER_OP):03X}" in text
    assert f"12'h{int(RegisterOffset.PIPELINE_MAP_CONSTANT):03X}" in text
    assert f"12'h{int(RegisterOffset.PIPELINE_GROUP_COUNT_LOW):03X}" in text
    # filter row count derives from INPUT_LENGTH, one row per 64-bit word
    assert "input_length_bytes[31:3]" in text
