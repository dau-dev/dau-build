def _run_cocotb_verilator(*, sources: tuple[Path, ...], hdl_toplevel: str, test_module: str, tmp_path: Path) -> None:
    runner_module = pytest.importorskip("cocotb_tools.runner", reason="cocotb runner not installed")
    runner = runner_module.get_runner("verilator")
    build_dir = tmp_path / hdl_toplevel
    runner.build(
        sources=sources,
        hdl_toplevel=hdl_toplevel,
        build_dir=build_dir,
        always=True,
        build_args=("--timing", "-Wno-fatal"),
    )
    runner.test(
        hdl_toplevel=hdl_toplevel,
        test_module=test_module,
        build_dir=build_dir,
        results_xml=str(tmp_path / f"{hdl_toplevel}-cocotb-results.xml"),
    )


def test_ohlcv_pipeline_top_chains_the_three_stages() -> None:
    text = generate_ohlcv_pipeline_top_sv()
    assert "module dau_ohlcv_pipeline" in text
    assert "dau_int32_keyed_predicate_filter filter_stage" in text
    assert "dau_int32_keyed_map_alu map_stage" in text
    assert "dau_int32_grouped_aggregation aggregation_stage" in text
    # downstream-of-compaction stages run last-driven
    assert text.count("cfg_row_count(64'd0)") == 2
    assert "GENERATED by dau_build.shell_tops" in text


